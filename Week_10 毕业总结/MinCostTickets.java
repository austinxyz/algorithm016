class Solution {
    public int mincostTickets(int[] days, int[] costs) {

        //Âà§Êñ≠
        if(days == null || days.length == 0 ||
                costs == null || costs.length == 0) {
            return 0;
        }

        //dpË°®Á§∫Âà∞‰∫ÜÂΩìÂ§©Ëä±ÁöÑÊúÄ‰ΩéÁ•®‰ª∑
        int[] dp = new int[days[days.length - 1] + 1];

        //base case: Á¨¨0Â§©‰∏ÄÂÆö‰∏çÁî®‰π∞Á•® ÂàôËä±Ë¥π0ÂÖÉ
        dp[0] = 0;
        //Ê†áËÆ∞‰∏Ä‰∏ãÈúÄË¶Å‰π∞Á•®ÁöÑÊó•Â≠ê
        for(int day: days) {
            dp[day] = Integer.MAX_VALUE;
        }

        for(int i = 1; i < dp.length; i++) {
            //‰∏çÈúÄË¶Å‰π∞Á•®
            if(dp[i] == 0) {
                //‰∏çÈúÄË¶Å‰π∞Á•®Ëä±Ë¥πÁöÑÈí±Â∞±ÊòØÂâç‰∏ÄÂ§©ÁöÑËä±Ë¥π
                dp[i] = dp[i - 1];
                continue;
            }

            int n1 = dp[i - 1] + costs[0];//ÂΩìÂ§©ÈúÄË¶Å‰π∞Á•®
            /**Â¶ÇÊûú‰ªäÂ§©Ë∑ùÁ¶ªÁ¨¨‰∏ÄÂ§©Â∑≤ÁªèË∂ÖËøá7Â§©‰∫Ü
             * ÂàôËä±Ë¥π: dp[i-7](7Â§©ÂâçÂ∑≤ÁªèËä±Ë¥πÁöÑÈí±)+cost[1](7Â§©Ââç‰π∞‰∫Ü‰∏ÄÂº†7Â§©ÁöÑÁ•®)
             * Âê¶ÂàôÂ∞±ÊòØÁõ¥Êé•Á¨¨‰∏ÄÂ§©‰π∞‰∫Ü‰∏ÄÂº†7Â§©Á•®
             */
            int n2 = i > 7 ? dp[i - 7] + costs[1] : costs[1];
            //30Â§©‰∏é7Â§© ÂêåÁêÜ
            int n3 = i > 30 ? dp[i - 30] + costs[2] :costs[2];

            dp[i] = Math.min(n1, Math.min(n2, n3));
        }
        //ÊúÄÂêé‰∏ÄÂ§©Ëä±Ë¥πÂ§öÂ∞ëÈí±
        return dp[days[days.length - 1]];


    }
}
